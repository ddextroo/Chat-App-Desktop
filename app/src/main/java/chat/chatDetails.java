package chat;

public class chatDetails {

    private String name;
    private String uid;

    public chatDetails(String name, String uid) {
        this.name = name;
        this.uid = uid;
    }

    public chatDetails() {
        this.name = name;
        this.uid = uid;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the uid
     */
    public String getUid() {
        return uid;
    }

    /**
     * @param uid the uid to set
     */
    public void setUid(String uid) {
        this.uid = uid;
    }
}
